def decode_source(encoded: str) -> str:
    return gzip.decompress(base64.b64decode(encoded.encode("ascii"))).decode("utf-8")


def write_component(name: str, encoded_source: str) -> Path:
    COMPONENT_CODE_DIR.mkdir(parents=True, exist_ok=True)
    path = COMPONENT_CODE_DIR / name
    path.write_text(decode_source(encoded_source), encoding="utf-8")
    return path


def find_competition_dir() -> Path:
    candidates = [
        Path("/kaggle/input/rogii-wellbore-geology-prediction"),
        Path("/kaggle/input/competitions/rogii-wellbore-geology-prediction"),
        Path.cwd() / "data/raw/rogii-wellbore-geology-prediction",
        Path.cwd() / "rogii-wellbore-geology-prediction",
    ]
    for candidate in candidates:
        if (
            (candidate / "train").is_dir()
            and (candidate / "test").is_dir()
            and (candidate / "sample_submission.csv").is_file()
        ):
            return candidate
    input_root = Path("/kaggle/input")
    if input_root.exists():
        for sample in input_root.glob("**/sample_submission.csv"):
            root = sample.parent
            if (root / "train").is_dir() and (root / "test").is_dir():
                return root
    raise FileNotFoundError("Could not locate ROGII competition data")


def run_component(
    name: str,
    script_path: Path,
    output_dir: Path,
    extra_env: dict[str, str] | None = None,
) -> Path:
    output_dir.mkdir(parents=True, exist_ok=True)
    env = os.environ.copy()
    env["PYTHONUNBUFFERED"] = "1"
    if extra_env:
        env.update(extra_env)
    start = time.time()
    print(f"\n=== Running {name} ===", flush=True)
    print(f"script={script_path}", flush=True)
    print(f"output_dir={output_dir}", flush=True)
    subprocess.run([sys.executable, str(script_path)], cwd=str(output_dir), env=env, check=True)
    csv_path = output_dir / "submission.csv"
    if not csv_path.is_file():
        raise FileNotFoundError(f"{name} did not write {csv_path}")
    print(f"=== {name} complete in {time.time() - start:.1f}s ===", flush=True)
    return csv_path


def read_submission(path: Path, sample_ids: pd.Series, label: str) -> pd.DataFrame:
    df = pd.read_csv(path)
    required = {"id", "tvt"}
    missing_cols = required.difference(df.columns)
    if missing_cols:
        raise ValueError(f"{label} missing columns: {sorted(missing_cols)}")
    df = df[["id", "tvt"]].copy()
    if df["id"].duplicated().any():
        raise ValueError(f"{label} has duplicate ids")
    if df["tvt"].isna().any():
        raise ValueError(f"{label} has null tvt predictions")
    sample_set = set(sample_ids.tolist())
    pred_set = set(df["id"].tolist())
    missing = sample_set.difference(pred_set)
    extra = pred_set.difference(sample_set)
    if missing or extra:
        raise ValueError(
            f"{label} id mismatch: missing={len(missing)} extra={len(extra)}"
        )
    ordered = pd.DataFrame({"id": sample_ids}).merge(df, on="id", how="left")
    if ordered["tvt"].isna().any():
        raise ValueError(f"{label} ordering merge produced nulls")
    return ordered


def sha256_file(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1 << 20), b""):
            digest.update(chunk)
    return digest.hexdigest()


def main() -> None:
    competition_dir = find_competition_dir()
    sample = pd.read_csv(competition_dir / "sample_submission.csv")[["id"]]
    print(f"competition_dir={competition_dir}", flush=True)
    print(f"sample_rows={len(sample)}", flush=True)

    sunny_script = write_component("sunny_physical_component.py", SUNNY_SOURCE_B64)
    v10_script = write_component("v10_artifact_component.py", V10_SOURCE_B64)

    sunny_csv = run_component(
        name="sunny_physical_pf",
        script_path=sunny_script,
        output_dir=WORKING_DIR / "component_sunny_physical_pf",
    )
    v10_csv = run_component(
        name="v10_artifact_stack",
        script_path=v10_script,
        output_dir=WORKING_DIR / "component_v10_artifact_stack",
        extra_env={
            "ROGII_INFERENCE_ONLY": "1",
            "ROGII_SAVE_ARTIFACTS": "0",
            "ROGII_RUN_TABICL": "1",
            "ROGII_OUTPUT_DIR": str(WORKING_DIR / "component_v10_artifact_stack"),
        },
    )

    sunny = read_submission(sunny_csv, sample["id"], "sunny_physical_pf")
    v10 = read_submission(v10_csv, sample["id"], "v10_artifact_stack")

    submission = sample.copy()
    submission["tvt"] = SUNNY_WEIGHT * sunny["tvt"] + (1.0 - SUNNY_WEIGHT) * v10["tvt"]
    output_path = WORKING_DIR / "submission.csv"
    submission.to_csv(output_path, index=False)

    diagnostic = sample.copy()
    diagnostic["tvt"] = 0.70 * sunny["tvt"] + 0.30 * v10["tvt"]
    diagnostic_path = WORKING_DIR / "diagnostic_submission_sunny70_v10_30.csv"
    diagnostic.to_csv(diagnostic_path, index=False)

    diff = (sunny["tvt"] - v10["tvt"]).abs()
    summary = {
        "sunny_weight": SUNNY_WEIGHT,
        "v10_weight": 1.0 - SUNNY_WEIGHT,
        "rows": int(len(submission)),
        "null_count": int(submission["tvt"].isna().sum()),
        "submission_path": str(output_path),
        "submission_sha256": sha256_file(output_path),
        "diagnostic_path": str(diagnostic_path),
        "sunny_path": str(sunny_csv),
        "v10_path": str(v10_csv),
        "component_abs_diff_mean": float(diff.mean()),
        "component_abs_diff_p95": float(diff.quantile(0.95)),
        "component_abs_diff_max": float(diff.max()),
    }
    print(json.dumps(summary, indent=2), flush=True)
    print(submission.head(8).to_string(index=False), flush=True)


if __name__ == "__main__":
    main()
